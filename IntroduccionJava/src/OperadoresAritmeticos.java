public class OperadoresAritmeticos {
    public static void main(String[] args) {
        System.out.println("*** Operadores Aritmético ***");
        int a = 5, b = 3, resultado;
        
        //Suma +
         resultado = a + b;
        System.out.println("resultado suma = " + resultado);

        //Resta -
        resultado = a - b;
        System.out.println("resultado resta = " + resultado);

        //Multiplicación
        resultado = a * b;
        System.out.println("resultado Multiplicación= " + resultado);

        //División
        resultado = a / b;
        System.out.println("resultado división = " + resultado);

        //Módulo
        resultado = a % b;
        System.out.println("resultado módulo = " + resultado);
    }
}
